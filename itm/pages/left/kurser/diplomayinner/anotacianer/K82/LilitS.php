  <p><b>   Շախմատ խաղի նախագծում </b></p>
  </center>

 <p align="justify"> Աշխատանքի նպատակն է ստեղծել շախմատ խաղ, որը բացի հիմնական խաղից ունի նաև հատուկ բաժին,որտեղ առաջադրվում են շախմատային հետաքրքիր խնդիրներ լուծելու համար, այդ խնդիրները լուծողը հնարավորություն ունի տեսնելու լուծման ճիշտ տարբերակը:Խնդիրների լուծման մեջ որոշակի շախմատային քայլեր է կատարում համակարգիչը     </p>
 