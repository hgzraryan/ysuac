<p align="center" style="color:#033"><strong>ԴիջիԹեք 2011 ցուցահանդես</strong></p>

 <p ><p align="justify" style="line-height:1.6">

 ԻՀՏ և ԿՀ-ի ամբիոնը ԵրԿՄԳՀԻ-ի հետ մասնակցեց ԴԻՋԻԹԵՔ 2011 ցուցահանդեսին, որը տեղի ունեցավ 07.10.2011-ից 09.10.2011թ-ը Կ.Դեմիրճյանի անվան մարզահամերգային համալիրում որտեղ ցուցադրվեց նաև համալսարանի ուսանողների կողմից նախագծված և պատրաստված ՏՏ սարքեր և ծրագրային ապահովում:</p>

 <p  align="justify" style="line-height:1.6">Օրինակ՝ <br />&nbsp;&nbsp;&nbsp; Կ-83 խմբի ուսանող <b>Արթուր Տերյանը</b>, ղեկավար` <u>Հակոբ Գևորգյան</u>, ներկայացրել էր տեխնոլոգիական գործընթացների ավտոմատացման առարկային վերաբերող լաբորատոր ստենդ, որը հնարավորություն է տալիս ուսանողին ուսումնասիրել տարբեր տվիչներից ինֆորմացիա ստանալու, ջերմություն կարգավորելու, ճնշում չափելու, շարժիչի աշխատանքը ղեկավարելու հնարավորությունները:   </p> 

